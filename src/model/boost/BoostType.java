package model.boost;

/**
 * The possible boost types.
 *
 * @version 1.0.0
 */
public enum BoostType {
	/** A coin that contributes to the score. */
	COIN,
	/** A flower that transforms Mario in his fire form. */
	FIRE_FLOWER,
	/** A mushroom that gives Mario an extra heart. */
	HEART_MUSHROOM,
	/** A star that transforms Mario in his star form. */
	STAR,
	/** A mushroom that transforms Mario in his super form. */
	SUPER_MUSHROOM,
}
